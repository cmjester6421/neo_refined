    name="neo-assistant",
    version="1.0.0",
    author="NEO Development Team",
    author_email="neo@example.com",
    description="NEO: Neural Executive Operator - Advanced AI Assistant",
    long_description=long_description,
    long_description_content_type="text/markdown",
